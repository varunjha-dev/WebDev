    /**
     * ➕ Addition Function - The Foundation of Mathematics
     * @param {number} a - First number (addend)
     * @param {number} b - Second number (addend)
     * @returns {number} Sum of a and b
     */
    const add = (a, b) => {
        return a + b;
    };

    //! Single function export - clean and focused
    module.exports = add;